class CellPhone: 

    def __init__(self, arg1, arg2, arg3):
        self.__manufact = arg1
        self.__model = arg2
        self.__retail_price = arg3

    def set_manufact(self):
        return self.__manufact


    def set_model(self):
        return self.__model

    def set_retail_price(self):
        return self.__retail_price
